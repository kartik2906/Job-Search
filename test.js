



function validation()
{ 
	
	var u = document.getElementById("userName").value;
	var p = document.getElementById("password").value;
	
	
	if (u === "" && p === "" ) 
	{
	document.getElementById('userName').style.borderColor="red";
		document.getElementById('password').style.borderColor="red";
		return false;
		
	}


	



}



	 




	
   

	








	
